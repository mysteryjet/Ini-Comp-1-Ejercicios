# Se pide  al usuario que ingrese los dos valores numéricos que requiere para hacer las operaciones
numero1 = float(input("Ingrese el primer número: ")) #se guardará el primer valor
numero2 = float(input("Ingrese el segundo número: ")) # se guardará el segundo valor

# Se realizan  las operaciones básicas
suma = numero1 + numero2
resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2

# Se muestra en termina  los resultados de las operaciones
print("La suma de los dos números es: ", suma)
print("La resta de los dos números es: ", resta)
print("La multiplicación de los dos números es: ", multiplicacion)
print("La división de los dos números es: ", division)
